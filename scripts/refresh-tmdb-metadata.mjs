#!/usr/bin/env node

import { createGunzip } from "node:zlib";
import { readFile, writeFile } from "node:fs/promises";
import { createInterface } from "node:readline";
import { Readable } from "node:stream";

const TOKEN = process.env.TMDB_ACCESS_TOKEN;
const GENERATED_TS = new URL("../lib/generated/imdb-top-titles.ts", import.meta.url);
const DATASET_BASE = "https://datasets.imdbws.com";

const items = await readGeneratedSeeds();
if (!TOKEN) {
  await refreshSeriesEpisodeCountsFromImdb(items);
  process.exit(0);
}

const refreshed = [];

for (const item of items) {
  if (!item.tmdbId) continue;
  const typePath = item.type === "series" ? "tv" : "movie";
  const details = await tmdbFetch(`/${typePath}/${item.tmdbId}`, {
    append_to_response: item.type === "series" ? "content_ratings,images" : "release_dates,images"
  });
  const images = details.images?.backdrops ?? [];
  const backdrop = pickWideBackdrop(images) ?? details.backdrop_path ?? item.backdropUrl;
  const poster = details.poster_path ?? item.posterFile;
  const next = {
    ...item,
    posterFile: poster,
    backdropUrl: backdrop,
    synopsis: cleanText(details.overview) || item.synopsis,
    genres: details.genres?.length ? details.genres.map((genre) => genre.name) : item.genres
  };

  if (item.type === "series") {
    const seasons = (details.seasons ?? [])
      .filter((season) => season.season_number > 0 && season.episode_count > 0)
      .slice(0, 12);
    next.duration = `${seasons.length || details.number_of_seasons || 1} Musim`;
    next.seasonDetails = [];

    for (const season of seasons) {
      const seasonDetails = await tmdbFetch(`/tv/${item.tmdbId}/season/${season.season_number}`);
      next.seasonDetails.push({
        seasonNumber: season.season_number,
        name: season.name || `Season ${season.season_number}`,
        synopsis: cleanText(season.overview || seasonDetails.overview),
        posterFile: season.poster_path || poster,
        episodes: (seasonDetails.episodes ?? []).map((episode) => ({
          episodeNumber: episode.episode_number,
          title: episode.name || `Episode ${episode.episode_number}`,
          synopsis: cleanText(episode.overview),
          duration: episode.runtime ? `${episode.runtime}m` : undefined,
          stillUrl: episode.still_path || backdrop,
          airDate: episode.air_date || undefined
        }))
      });
    }
    delete next.seasonEpisodeCounts;
  }

  refreshed.push(next);
}

await writeFile(
  GENERATED_TS,
  `import type { TitleSeed } from "@/lib/data";

// Generated by scripts/import-imdb-top.mjs and refreshed by scripts/refresh-tmdb-metadata.mjs.
export const imdbTopTitleSeeds = ${JSON.stringify(refreshed, null, 2)} satisfies readonly TitleSeed[];
`
);

console.log(`Refreshed ${refreshed.length} TMDB titles with posters, wide backdrops, and series seasons.`);

async function refreshSeriesEpisodeCountsFromImdb(items) {
  const seriesIds = new Set(items.filter((item) => item.type === "series" && item.imdbId).map((item) => item.imdbId));
  const countsBySeries = new Map();

  for await (const row of tsvRows(`${DATASET_BASE}/title.episode.tsv.gz`)) {
    if (row.tconst === "tconst" || !seriesIds.has(row.parentTconst)) continue;
    const seasonNumber = Number(row.seasonNumber);
    const episodeNumber = Number(row.episodeNumber);
    if (!Number.isFinite(seasonNumber) || !Number.isFinite(episodeNumber) || seasonNumber <= 0 || episodeNumber <= 0) continue;

    const seasons = countsBySeries.get(row.parentTconst) ?? new Map();
    seasons.set(seasonNumber, Math.max(seasons.get(seasonNumber) ?? 0, episodeNumber));
    countsBySeries.set(row.parentTconst, seasons);
  }

  const refreshed = items.map((item) => {
    if (item.type !== "series" || !item.imdbId) return item;
    const seasons = countsBySeries.get(item.imdbId);
    if (!seasons?.size) return item;
    const seasonEpisodeCounts = Array.from(seasons)
      .sort(([a], [b]) => a - b)
      .map(([, count]) => count);
    const next = {
      ...item,
      duration: `${seasonEpisodeCounts.length} Musim`,
      seasonEpisodeCounts
    };
    delete next.seasonDetails;
    return next;
  });

  await writeGeneratedSeeds(refreshed, "Generated by scripts/import-imdb-top.mjs and refreshed from IMDb episode datasets.");
  console.log(`TMDB_ACCESS_TOKEN not found. Refreshed season/episode counts from IMDb for ${countsBySeries.size} series.`);
}

async function readGeneratedSeeds() {
  const source = await readFile(GENERATED_TS, "utf8");
  const match = source.match(/export const imdbTopTitleSeeds = (\[[\s\S]*?\]) satisfies/);
  if (!match) throw new Error("Could not parse generated seeds.");
  return JSON.parse(match[1]);
}

async function writeGeneratedSeeds(items, comment) {
  await writeFile(
    GENERATED_TS,
    `import type { TitleSeed } from "@/lib/data";

// ${comment}
export const imdbTopTitleSeeds = ${JSON.stringify(items, null, 2)} satisfies readonly TitleSeed[];
`
  );
}

async function tmdbFetch(path, params = {}) {
  const url = new URL(`https://api.themoviedb.org/3${path}`);
  Object.entries(params).forEach(([key, value]) => url.searchParams.set(key, value));
  const response = await fetch(url, {
    headers: {
      accept: "application/json",
      authorization: `Bearer ${TOKEN}`
    }
  });
  if (!response.ok) throw new Error(`TMDB ${path} failed: ${response.status}`);
  return response.json();
}

function pickWideBackdrop(images) {
  return images
    .filter((image) => image.aspect_ratio >= 1.6 && image.file_path)
    .sort((a, b) => (b.vote_average ?? 0) - (a.vote_average ?? 0) || (b.width ?? 0) - (a.width ?? 0))[0]?.file_path;
}

function cleanText(value) {
  return String(value ?? "").replace(/\s+/g, " ").trim();
}

async function* tsvRows(url) {
  const response = await fetch(url);
  if (!response.ok || !response.body) throw new Error(`Failed to download ${url}: ${response.status}`);
  const stream = Readable.fromWeb(response.body).pipe(createGunzip());
  const rl = createInterface({ input: stream, crlfDelay: Infinity });
  let headers = null;
  for await (const line of rl) {
    const cols = line.split("\t");
    if (!headers) {
      headers = cols;
      continue;
    }
    yield Object.fromEntries(headers.map((header, index) => [header, cols[index]]));
  }
}
